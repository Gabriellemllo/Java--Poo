public class Universidade {
    public string nomeAluno;
    public double nota;
    public double ra;
    public string disciplina;
    public string turno;

    public Universidade(string nomeAluno, double nota, double ra, string disciplina, string turno){
    this.nomeAluno = nomeAluno;
    this.nota = nota;
    this.ra = ra;
    this.disciplina = disciplina;
    this.turno = turno;
    }
}